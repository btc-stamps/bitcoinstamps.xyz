function stripFrontmatter(content) {
  return content.replace(/^---\s*\n[\s\S]*?\n---\s*\n/, '');
}

function extractTitle(content) {
  const fm = content.match(/^---\s*\n([\s\S]*?)\n---\s*\n/);
  if (fm) {
    const t = fm[1].match(/^title:\s*["']?(.+?)["']?\s*$/m);
    if (t) return t[1];
  }
  const h1 = content.match(/^#\s+(.+)$/m);
  return h1 ? h1[1] : null;
}

// Reduce Vue/VitePress component markup to plain text.
function stripComponents(md) {
  return md
    // <EntityMention ...>slot</EntityMention> -> slot
    .replace(/<EntityMention\b[^>]*>([\s\S]*?)<\/EntityMention>/g, '$1')
    // self-closing <EntityMention entity="x" /> -> x
    .replace(/<EntityMention\b[^>]*\bentity=["']([^"']+)["'][^>]*\/>/g, '$1')
    // functional self-closing components with no useful text
    .replace(/<SmartStructuredData\b[^>]*\/>/g, '')
    .replace(/<SipRegistry\b[^>]*\/>/g, '')
    // any other leftover self-closing custom component tags
    .replace(/<[A-Z][A-Za-z0-9]*\b[^>]*\/>/g, '')
    // collapse 3+ blank lines
    .replace(/\n{3,}/g, '\n\n');
}

function sourceUrl(relPath) {
  // en/foo/index.md -> /en/foo/ ; en/foo/bar.md -> /en/foo/bar
  let p = relPath.replace(/\\/g, '/').replace(/\.md$/, '');
  p = p.replace(/\/index$/, '/');
  return `${SITE}/en/${p}`.replace(/([^:])\/\//g, '$1/');
}

function listAllMd(dir) {
  const out = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) out.push(...listAllMd(full));
    else if (entry.name.endsWith('.md')) out.push(path.relative(EN_DIR, full));
  }
  return out;
}

function main() {
  console.log('Generating llms-full.txt from docs/en corpus...\n');

  const all = new Set(listAllMd(EN_DIR).map((p) => p.replace(/\\/g, '/')));
  const ordered = ORDER.filter((p) => all.has(p));
  const remaining = [...all].filter((p) => !ORDER.includes(p)).sort();
  const sequence = [...ordered, ...remaining];

  if (remaining.length > 0) {
    console.log(`Note: ${remaining.length} unlisted file(s) appended: ${remaining.join(', ')}`);
  }

  let body = HEADER;
  let included = 0;

  for (const rel of sequence) {
    const full = path.join(EN_DIR, rel);
    if (!fs.existsSync(full)) continue;
    let content = fs.readFileSync(full, 'utf8');
    const title = extractTitle(content) || rel;
    content = stripComponents(stripFrontmatter(content)).trim();
    if (!content) continue;

    body += `\n\n<!-- source: ${rel} -->\n`;
    body += `# ${title}\n`;
    body += `Source: ${sourceUrl(rel)}\n\n`;
    body += content;
    body += '\n\n---\n';
    included += 1;
    console.log(`  + ${rel}`);
  }

  fs.writeFileSync(OUTPUT, body, 'utf8');
  const kb = (fs.statSync(OUTPUT).size / 1024).toFixed(1);
  console.log(`\nWrote ${OUTPUT}`);
  console.log(`  ${included} sections, ${kb} KB`);
}

main();
